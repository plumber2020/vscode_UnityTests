	uint16_t pss_crc_module_MZ2;					/* КС СНП модуля */
	
	uint16_t soft_crc_MK;							/* КС ПО модуля контроля */
	uint16_t pss_crc_module_MK;						/* КС СНП модуля */
	
	uint16_t soft_crc_Commutator1;					/* КС ПО коммутатора 1 */
	uint16_t pss_crc_Commutator1;					/* КС СНП модуля */
	
	uint16_t soft_crc_Commutator2;					/* КС ПО коммутатора 2 */
	uint16_t pss_crc_Commutator2;					/* КС СНП модуля */
	
