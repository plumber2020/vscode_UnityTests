	int16_t Tbl_2;									/* Температура блока, полученная от 2-го модуля защиты */
	
	uint16_t Usupply_3;								/* 1-е напряжение питания блока модуля управления */
	uint16_t Usupply_4;								/* 2-е напряжение питания блока модуля управления */
	
	uint16_t soft_crc;								/* КС ПО модуля управления */
	uint16_t pss_crc_engine;						/* КС СНП двигателя */
	uint16_t pss_crc_module;						/* КС СНП модуля */
	
