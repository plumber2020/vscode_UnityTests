	int16_t adc_Usupply_2;							/* Значение кода АЦП 1-го напряжения питания */
	
	uint16_t adc_ctrl_rpms_1;						/* Значение АЦП в канале контроля 1-го датчика оборотов */
	uint16_t adc_ctrl_rpms_2;						/* Значение АЦП в канале контроля 2-го датчика оборотов */
	
	uint16_t CAN1_status;							/* Значение регистра статуса 1-го канала CAN */
	uint16_t CAN1_err_over;							/* Флаг получения ошибки ERROR OVER */
	uint16_t CAN1_on_off;							/* Флаг включения/выключения 1-го канала CAN (1 - канал включен, 0 - канал выключен)*/
	uint16_t CAN2_status;							/* Значение регистра статуса 2-го канала CAN */
	uint16_t CAN2_err_over;							/* Флаг получения ошибки ERROR OVER */
	uint16_t CAN2_on_off;							/* Флаг включения/выключения 2-го канала CAN (1 - канал включен, 0 - канал выключен)*/
	
