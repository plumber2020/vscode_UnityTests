	uint16_t ADC_angle_3;							/* 2-й канал угломера */
	uint16_t ADC_angle_4;							/* Регистр состояния 2-го канала угломера */
	
	uint16_t Usupply_1;								/* Напряжение питания блока, полученное от 1-го модуля защиты */
