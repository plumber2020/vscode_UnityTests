void BitIoRewind ( void )
{	
		// re-init  buffers
    //	mix1	putbuf = putlen = getbuf= getlen=0;
		//	mix1	bitinlen=bitoutlen=0;
    putbuf = putlen = 0;
		bitoutlen = 4; // need for correct write IDK preambule from 0 to 3 bytes
}


//
//****************************************************************
//
//		Put8bit - при наборе 8-и значащих бит, требующих передачи,
//			выкладывает буфер putbuf в простой буфер передатчика,
//			используя ByteOut()
//	param	data - значение, к-рое Н. передать (значащих бит <= 8)
//			bits - количество бит значения
//	return	нет
//	in		нет
//	out		putbuf - буфер для значения или его части
//			putlen - число значащих бит в буфере putbuf
//
//****************************************************************
//
void Put8bit (uint32_t data, int32_t bits)
{
	data &= 0xFFFFFFFFu >> ( 32 - bits );	//	выделили bits бит в data
	putbuf |= data << putlen;			//	положилив буфер с учетом уже имеющихся значащих бит
	putlen += bits;						//	увеличили число значащих бит в буфере

	if ( putlen >= 24 )
	{	//	можно выкладывать в простой буфер передатчика
		ByteOut((int8_t)putbuf);	//	выкладываем старые биты (если были) и часть новых
		putbuf >>= 8;			//	оставляем только не выложенные значащие биты
		putlen -= 8;
	}
	if ( putlen >= 16 )
	{	//	можно выкладывать в простой буфер передатчика
		ByteOut((int8_t)putbuf);	//	выкладываем старые биты (если были) и часть новых
		putbuf >>= 8;			//	оставляем только не выложенные значащие биты
		putlen -= 8;
	}
	if ( putlen >= 8 )
	{	//	можно выкладывать в простой буфер передатчика
		ByteOut((int8_t)putbuf);	//	выкладываем старые биты (если были) и часть новых
		putbuf >>= 8;			//	оставляем только не выложенные значащие биты
		putlen -= 8;
	}
}

//
//****************************************************************
//
//		Put16bit - выделяет bits младших бит из data и по 8 или
//			меньше бит в Put8bit()
//	param	data - значение, к-рое Н. передать (значащих бит <= 16)
//			bits - количество бит значения
//	return	нет
//	in		нет
//	out		нет
//
//****************************************************************
//
void Put16bit (uint16_t data, int16_t bits)
{ // send up to 16 bits
	if ( bits > 8 )
	{
		Put8bit(data,8);
		data >>= 8;
		bits -= 8;
  }
    Put8bit(data,bits);
}
//
//****************************************************************
//
//		Put32bit - выделяет bits младших бит из data и по 8 или
//			меньше бит в Put8bit()
//	param	data - значение, к-рое Н. передать (значащих бит <= 32)
//			bits - количество бит значения
//	return	нет
//	in		нет
//	out		нет
//
//****************************************************************
//
void Put32bit (uint32_t data, int32_t bits)
{ // send up to 32 bits

	if ( bits > 24 )
	{
		Put8bit(data,24);
		data >>= 24;
		bits -= 24;
  }
	if ( bits > 16 )
	{
		Put8bit(data,16);
		data >>= 16;
		bits -= 16;
  }
	if ( bits > 8 )
	{
		Put8bit(data,8);
		data >>= 8;
		bits -= 8;
  }
	Put8bit(data,bits);
  
}

//
//****************************************************************
//
//		BitOutFlush - выравнивает нулями на границу байта через
//			Put8bit() и обнуляет буфер значений и число значащих
//			бит в нем
//	param	нет
//	return	нет
//	in		нет
//			putbuf - буфер для значения или его части
//			putlen - число значащих бит в буфере putbuf
//
//****************************************************************
//
void BitOutFlush (void)
{
	if(putlen)		//	Н. добавить нулями до байта
		Put8bit(0,8);
	putbuf = putlen = 0;
}
