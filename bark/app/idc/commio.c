#include	"commio.h"


uint32_t	putlen;					//	число значащих (требуемых передачи) бит в putbuf
	uint64_t				putbuf;					//	буфер для выкладывания в простой буфер передатчика

uint8_t		bitoutbuf[512];	//	простой буфер передатчика

volatile uint16_t	bitoutlen;			//	длина данных (пакета) в простом буфере передатчика

//
//****************************************************************
//
//		PackBlock - выделение из монстовой структуры значений
//			данных и числа бит этих данных для передачи по RS
//	param	*b - адрес блока данных (нужен, если в монстровой
//				 структуре первый элемент описан как UW или SW)
//			*Desc - адрес монстровской структуры
//	return	нет
//	in		нет
//	out		нет
//
//****************************************************************
//

uint32_t readByPtr(uint32_t *adr)
{
	TVariant32 data;
	uint8_t *u8ptr = (uint8_t*)adr;
	

	data.byte[0] = *u8ptr;
	data.byte[1] = *(u8ptr+1);
	data.byte[2] = *(u8ptr+2);
	data.byte[3] = *(u8ptr+3);
	
	return data.dword;
}

void PackBlock (void *b, MONSTR_t *Desc)
{
	uint32_t	*p = b;
	uint32_t	d;			//	5 младших бит - число передаваемых бит из значения
	uint32_t	bits,		//	число передаваемых бит из значения
			v,			//	передаваемое значение
			val=0;
	
	
	while ( 1 )
	{
		if ( (d= *Desc++) == _monstr_E )	//	читаем дескриптор
			return;		// конец списка
		if ( d & _monstr_A )				//	адрес значения в следующем слове
		{
			p = (void *) *Desc++;			//	адрес значения
		}
		if ( _monstr_B == (d & 0xfffff800) ) 
			v = val >> ((d>>6) & 31);		//	если битовое поле, берем из предыдущего (val) 
		else
		{
			//v = val = *p++;					//	передаваемое значение
			v = val = readByPtr(p);
			p++;
		}
		if ( 0 != (bits = (d & 63)) )		//	число передаваемых бит (6 младших в d) не 0  
			Put32bit(v,bits);				
	}
}


